local M = {}

function M.get(c)
	return {
		Comment = { fg = c.comment, italic = true },
		Constant = { fg = c.cyan },
		String = { fg = c.teal },
		Character = { fg = c.teal },
		Number = { fg = c.cyan },
		Boolean = { fg = c.orange },
		Float = { fg = c.orange },
		Identifier = { fg = c.white },
		Function = { fg = c.fn },
		Statement = { fg = c.kw },
		Conditional = { fg = c.kw },
		Repeat = { fg = c.kw },
		Label = { fg = c.kw },
		Operator = { fg = c.grey2 },
		Keyword = { fg = c.kw },
		Exception = { fg = c.kw },
		PreProc = { fg = c.kw },
		Include = { fg = c.variant == "noir" and c.yellow or c.kw },
		Define = { fg = c.kw },
		Macro = { fg = c.orange },
		PreCondit = { fg = c.kw },
		Type = { fg = c.yellow },
		StorageClass = { fg = c.kw },
		Structure = { fg = c.yellow },
		Typedef = { fg = c.yellow },
		Special = { fg = c.orange },
		SpecialChar = { fg = c.orange },
		Tag = { fg = c.grey2 },
		Delimiter = { fg = c.grey2 },
		SpecialComment = { fg = c.comment, italic = true },
		Debug = { fg = c.orange },
		Underlined = { underline = true },
		Ignore = { fg = c.grey1 },
		Error = { fg = c.red },
		Todo = { fg = c.cyan, bold = true },
		Added = { fg = c.green },
		Changed = { fg = c.blue },
		Removed = { fg = c.red },
	}
end

return M
